///VariableDatetype

package helloworld;
public class VariableDatetype {
    public static void main(String[] args)        
    {
        boolean b=true;
        char c='a';
        short s=32675;
        int i=122456;
        float f=10.30f;
        double d=96.70;
        
        System.out.println("b="+b);
        System.out.println("c="+c);
        System.out.println("s="+s);
        System.out.println("i="+i);
        System.out.println("f="+f);
        System.out.println("d="+d);
           
    }
}
